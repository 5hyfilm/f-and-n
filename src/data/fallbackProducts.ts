// Helper functions for fallback products
export const findFallbackProductByBarcode = (
  barcode: string
): Product | undefined => {
  const cleanBarcode = barcode.trim().replace(/[^0-9]/g, "");
  return FALLBACK_PRODUCTS.find(
    (product) => product.barcode.replace(/[^0-9]/g, "") === cleanBarcode
  );
};

export const getFallbackStats = () => {
  const totalProducts = FALLBACK_PRODUCTS.length;
  const activeProducts = FALLBACK_PRODUCTS.filter(
    (p) => p.status === ProductStatus.ACTIVE
  ).length;
  const categories = [...new Set(FALLBACK_PRODUCTS.map((p) => p.category))]
    .length;
  const brands = [...new Set(FALLBACK_PRODUCTS.map((p) => p.brand))].length;

  return {
    totalProducts,
    activeProducts,
    categories,
    brands,
  };
};
